export type IchimokuIndicators = {
    tenkan: number | undefined
    kijun: number | undefined
    ssa: number | undefined
    ssb: number | undefined
    chikou: number | undefined
}

export type IchimokuCandlestickData = CandlestickData & IchimokuIndicators

const toCandlestickData = ([openTimeMs, open, high, low, close]: Array<string | number>): IchimokuCandlestickData => ({
    time: ((openTimeMs as number) / 1000) as UTCTimestamp,
    open: parseFloat(open as string),
    high: parseFloat(high as string),
    low: parseFloat(low as string),
    close: parseFloat(close as string),
    tenkan: undefined,
    kijun: undefined,
    ssa: undefined,
    ssb: undefined,
    chikou: undefined,
})

const tenkan = (i: number, items: Array<IchimokuCandlestickData>) => {
    if (i - 9 < 0) return undefined
    const slice = items.slice(i - 9, i)
    const { max, min } = slice.reduce(
        ({ max, min }, current) => ({
            max: Math.max(max, current.high),
            min: Math.min(min, current.low),
        }),
        { max: slice[0].high, min: slice[0].low },
    )
    return (max + min) / 2
}

const kijun = (i: number, items: Array<IchimokuCandlestickData>) => {
    if (i - 26 < 0) return undefined
    const slice = items.slice(i - 26, i)
    const { max, min } = slice.reduce(
        ({ max, min }, current) => ({
            max: Math.max(max, current.high),
            min: Math.min(min, current.low),
        }),
        { max: slice[0].high, min: slice[0].low },
    )
    return (max + min) / 2
}

const ssa = (i: number, items: Array<IchimokuCandlestickData>) => {
    if (i - 26 < 0) return undefined
    const item = items[i - 26]
    return (item.tenkan! + item.kijun!) / 2
}

const ssb = (i: number, items: Array<IchimokuCandlestickData>) => {
    if (i - (52 + 26) < 0) return undefined
    const slice = items.slice(i - (52 + 26), i - 52)
    const { max, min } = slice.reduce(
        ({ max, min }, current) => ({
            max: Math.max(max, current.high),
            min: Math.min(min, current.low),
        }),
        { max: slice[0].high, min: slice[0].low },
    )
    return (max + min) / 2
}

const chikou = (i: number, items: Array<IchimokuCandlestickData>) => {
    if (i + 26 >= items.length) return undefined
    return items[i + 26].close
}

export const dailyData = daily
    .map(toCandlestickData)
    .map((item, i, items) => ({
        ...item,
        tenkan: tenkan(i, items),
        kijun: kijun(i, items),
        ssb: ssb(i, items),
        chikou: chikou(i, items),
    }))
    .map((item, i, items) => ({
        ...item,
        ssa: ssa(i, items),
    }))
